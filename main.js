    // Event listener for search input
    document.getElementById('search').addEventListener('input', function () {
        const searchTerm = this.value.toLowerCase(); // Get the input and convert it to lowercase
        const chefs = document.querySelectorAll('.team-item'); // Select all chef cards

        let found = false; // Track if any chef was found

        chefs.forEach(function (chef) {
            const specialty = chef.querySelector('h6').textContent.toLowerCase(); // Get the chef's specialty

            // Check if the specialty includes the search term
            if (specialty.includes(searchTerm)) {
                chef.classList.remove('d-none'); // Show chef card by removing d-none
                found = true; // A chef was found
            } else {
                chef.classList.add('d-none'); // Hide chef card by adding d-none
            }
        });

        // Optional: Alert if no chefs are found
        if (!found && searchTerm.length > 0) {
            alert('No chefs found for the search term: ' + searchTerm);
        }
    });

    











